#include <stdio.h>

int main()
{

    // Integer
    int a;
    scanf("%d", &a);


    // Floating Point Number
    float b;
    scanf("%f", &b);


    // Character
    char c;
    scanf(" %c", &c);

    printf("The integer value: %d\n", a);
    printf("The floating point value: %f\n", b);
    printf("The character value: %c", c);

    return 0;
}